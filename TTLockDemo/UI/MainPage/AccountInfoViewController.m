- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (!cell) {
        cell = [[UITableViewCell alloc]initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier];
    
    }
    cell.selectionStyle = UITableViewCellSelectionStyleNone;
    
    
    switch (indexPath.row) {
        case 0:
        {
            
            cell.detailTextLabel.text = [SettingHelper getOpenID];
            cell.textLabel.text = @"OpenId";
            break;
        }
        case 1:
            
            cell.detailTextLabel.text = TTAppkey;
            cell.textLabel.text = @"AppKey";
            
            break;
        case 2:
        {
            
            cell.detailTextLabel.text = [SettingHelper getAccessToken];
            cell.textLabel.text = @"AccessToken";
            break;
        }
            
        default:
            break;
    }


    return cell;
}


